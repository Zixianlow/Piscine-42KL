int	ft_sqrt(int nb)
{
	int	i;

	i = 2;
	if (nb < 0 || nb > 2147395600)
		return (0);
	else if (nb == 1)
		return (1);
	while (i * i < nb)
		i++;
	if (i * i == nb)
		return (i);
	return (0);
}
